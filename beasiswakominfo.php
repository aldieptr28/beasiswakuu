                    disampaikan secara resmi melalui akun masing-masing pada portal website Beasiswa Kominfo.

                    Jadwal seleksi masuk mitra perguruan tinggi Kementerian Kominfo tahun 2023 dapat diakses pada
                    masing-masing situs resmi penerimaan mitra perguruan tinggi dan situs Beasiswa Kominfo pada
                    beasiswa.kominfo.go.id.



                    Beasiswa S2 Luar Negeri

                    Pendaftaran Beasiswa S2 Luar Negeri Kementerian Komunikasi dan Informatika TA 2023 dibuka bagi
                    aparatur pemerintah pusat/anggota TNI/Polri dan non-PNS dari kementerian/LPNK/BUMN/swasta/pelaku
                    startup dengan latar belakang pekerjaan bidang TIK yang berminat untuk melanjutkan studi magister di
                    bidang studi terkait teknologi informasi dan komunikasi.

                    Pada tahap I, pendaftaran untuk program beasiswa S2 luar negeri dibuka hingga 25 Maret 2023 untuk
                    program studi tertentu di lima negara tujuan studi, yaitu Tiongkok, India, Belanda, Hungaria, dan
                    Jepang. Calon pendaftar agar memperhatikan batas waktu pendaftaran ke Kementerian Kominfo serta
                    perguruan tinggi yang dituju.

                    Persyaratan pelamar Beasiswa S2 Luar Negeri Kementerian Kominfo terbagi menjadi persyaratan umum
                    yang berlaku untuk seluruh skema beasiswa di seluruh universitas dan negara tujuan studi. Selain
                    itu, ada persyaratan khusus yang berlaku secara spesifik untuk negara tujuan dan perguruan tinggi
                    tertentu.

                    Pelamar diharuskan mendaftarkan ke kampus tujuan untuk masa perkuliahan Agustus/September 2023 atau
                    Januari/Februari 2024.

                    Lima kampus yang bekerja sama dengan Kominfo adalah Tsinghua University, Tiongkok; International
                    Institute of Information Technology Bangalore (IIITB), India; Eötvös Loránd University (ELTE),
                    Hungaria; University of Twente, Belanda; dan The University of Electro Communications (UEC), Tokyo,
                    Jepang.

                    Persyaratan lebih lengkapnya bisa dipantau di situs Beasiswa Kominfo beasiswa.kominfo.go.id.
