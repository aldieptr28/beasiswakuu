                    Kementerian Komunikasi dan Informatika melalui Badan Penelitian dan Pengembangan Sumber Daya Manusia
                    kembali membuka Program Beasiswa S2 Dalam dan Luar Negeri Tahun 2022. Program ini merupakan salah
                    satu wujud komitmen Kementerian Komunikasi dan Informatika untuk pengembangan kapasitas sumber daya
                    manusia di bidang Teknologi Informasi dan Komunikasi. Program dibuka untuk mendukung program
                    prioritas percepatan Transformasi Digitalisasi Nasional yang meliputi 4 (empat) sektor strategis,
                    yaitu infrastruktur digital, pemerintahan digital, ekonomi digital dan masyarakat digital.

                    Program Beasiswa ini terbuka bagi aparatur pemerintah pusat dan daerah termasuk anggota TNI dan
                    POLRI serta masyarakat umum dari instansi swasta yang memiliki latar belakang pekerjaan di sektor
                    TIK atau pelaku start-up lokal.

                    Kementerian Kominfo menggandeng sembilan perguruan tinggi nasional dan lima kampus mancanegara
                    sebagai mitra kerja sama program beasiswa S2.
                    Badan Penelitian dan Pengembangan (Balitbang) SDM Kementerian Kominfo pada 2023 kembali membuka
                    pendaftaran program Beasiswa S2 Dalam dan Luar Negeri Kementerian Kominfo tahap I tahun anggaran
                    2023.

                    Program tersebut merupakan upaya Kominfo dalam mengembangkan sumber daya manusia bidang komunikasi
                    dan informatika serta kapasitas kepemimpinan dan pembuatan kebijakan bagi aparatur sipil negara
                    (ASN)/Anggota TNI/Polri, dan masyarakat umum atau profesional.

                    Adapun sejak 2007, program beasiswa itu diadakan agar masyarakat luas dapat menempuh jenjang
                    pendidikan yang lebih tinggi.

                    Beasiswa S2 Dalam Negeri:

                    Beasiswa S2 Dalam Negeri diselenggarakan bagi PNS pusat maupun daerah, serta anggota TNI/Polri dan
                    juga masyarakat umum dari kalangan profesional dan instansi swasta untuk melanjutkan pendidikan
                    pascasarjana dalam aspek pengembangan kompetensi di bidang Teknologi Informasi dan Komunikasi (TIK).
                    Untuk itu, Kementerian Kominfo menggandeng sembilan perguruan tinggi sebagai menjadi mitra kerja
                    sama penyelenggaraan program beasiswa.

                    A. Adapun persyaratan umum yang harus dipenuhi untuk mengikuti program beasiswa itu adalah:

                    Masa kerja minimum dua tahun;
                    Belum memiliki gelar S2 dan tidak sedang mengikuti program pendidikan S2 dari lembaga lain;
                    Persyaratan lainnya, mengikuti persyaratan masing-masing perguruan tinggi yang dipilih;
                    Pendaftar beasiswa hanya diperkenankan untuk mendaftar pada kelas reguler.
                    Outline rencana tugas akhir maksimal 1 halaman (judul, latar belakang, tujuan dan manfaat yang
                    sesuai dengan program Transformasi Digitalisasi Nasional).
                    B. Persyaratan khusus bagi PNS, TNI/Polri dalam program Beasiswa S2 Dalam Negeri adalah:

                    Pegawai Negeri Sipil (PNS) pada instansi pemerintah pusat dan daerah, TNI/Polri berstatus aktif;
                    Masa kerja minimum dua tahun (terhitung sejak menjadi CPNS atau setara);
                    Berusia maksimum 45 tahun pada saat mendaftarkan diri;
                    Mendapatkan izin dari pejabat berwenang untuk menjalani pendidikan sesuai dengan ketentuan
                    masing-masing instansi;
                    Persyaratan standar IPK minimal 3,00;
                    Tidak ditujukan bagi PNS dengan jabatan fungsional pengajar pada instansi sektor pendidikan;
                    Ketentuan lain yang harus dipenuhi untuk mengikuti program Beasiswa S2 Dalam Negeri sebagai berikut:

                    Tugas dan fungsinya dalam pekerjaan berkaitan dengan salah satu program studi yang akan ditempuh,
                    yakni pelayanan informasi dan kehumasan pemerintah, tata kelola teknologi informasi dan
                    komunikasi/keamanan informasi, penyusunan strategi dan kebijakan bidang digital serta peningkatan
                    daya saing nasional dalam sektor ekonomi digital.
                    Pendaftar beasiswa hanya diperkenankan untuk mendaftar pada kelas regular.
                    Tidak sedang menjalani hukuman disiplin sedang atau berat dan tidak pernah dijatuhi hukuman disiplin
                    paling kurang tingkat sedang dalam satu tahun terakhir;
                    Memenuhi persyaratan penetapan tugas belajar sebagaimana dimaksud Surat Edaran Menpan RB nomor 28
                    tahun 2021 tentang Pengembangan Kompetensi Pegawai Negeri Sipil melalui Jalur Pendidikan.
                    C. Persyaratan khusus bagi masyarakat umum dalam program Beasiswa S2 Dalam Negeri adalah:

                    Warga Negara Indonesia (WNI);
                    Berusia maksimum 45 tahun pada saat mendaftarkan diri;
                    Latar belakang pekerjaan di bidang teknologi informasi dan komunikasi dan/atau pelaku startup;
                    Masa kerja minimum 2 tahun;
                    Menyertakan surat rekomendasi dari pimpinan, dosen, atau tokoh lain yang memiliki kredibilitas di
                    bidangnya dan mengenal pelamar dengan baik;
                    Persyaratan standar IPK minimal 3,00;
                    Persyaratan lainnya mengikuti persyaratan masing-masing perguruan tinggi yang dipilih.
                    Program studi yang dapat dipilih dalam program beasiswa ini antara lain: Magister Teknologi
                    Informasi (MTI) di Fakultas Ilmu Komputer Universitas Indonesia (UI) dengan fokus area Tata Kelola
                    TIK bagi PNS dan Start-up Digital bagi Masyarakat Umum; Magister Manajemen Keamanan Jaringan
                    Informasi (MKJI) - Departemen Teknik Elektro, Fakultas Teknik Universitas Indonesia dengan fokus
                    area Pengembangan Cyber Security bagi PNS; Master of Arts in Digital Transformation and
                    Competitiveness (MA in DTC), Departemen llmu Hubungan Internasional, FISIP Universitas Gadjah Mada
                    dengan fokus area pengembangan strategi dan kebijakan peningkatan daya saing nasional dalam sektor
                    ekonomi digital bagi PNS dan profesional; serta Magister Ilmu Komunikasi di FISIP UI, UGM, UNS,
                    Unair, Unand, dan Unhas.

                    Informasi kampus yang bekerja sama dalam program Beasiswa S2 Dalam Negeri Kominfo bisa dicek dalam
