                    Bagi mahasiswa, PIP diharapkan dapat:

                    meningkatkan perluasan akses dan kesempatan belajar di perguruan tinggi bagi mahasiswa warga negara
                    Indonesia yang tidak mampu secara ekonomi;
                    meningkatkan prestasi Mahasiswa pada bidang akademik dan nonakademik;
                    menjamin keberlangsungan studi mahasiswa yang berasal dari daerah terdepan, terluar, atau
