                    Tujuan Utama Program Beasiswa Pendidikan Indonesia


                    Pertama, Beasiswa Pendidikan Indonesia bertujuan untuk meningkatkan kemampuan dan kompetensi sumber
                    daya manusia Indonesia yang mendukung percepatan pembangunan Negara Kesatuan Republik Indonesia.

                    Kedua, mengoptimalkan pemanfaatan Dana Pengembangan Pendidikan Nasional (DPPN) yang ada di LPDP
                    melalui pemberian beasiswa bergelar (degree) untuk jenjang pendidikan S-1, S-2, S-3, dan non gelar
                    (non-degree) bagi Warga Negara Indonesia yang memenuhi persyaratan pada perguruan tinggi terbaik di
                    dalam negeri ataupun di luar negeri.




                    Siapa Sasaran Beasiswa Pendidikan Indonesia?


                    Semua warga negara Indonesia berhak untuk mendaftarkan diri terutama untuk siswa Calon Guru SMK,
                    siswa dan Mahasiswa Berprestasi, Guru, Dosen, Tenaga Kependidikan dan Pelaku Budaya.



